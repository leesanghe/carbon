package com.carina.cbs.cbi.vo;

public class CbiVO {
    
	
	
	
	private String municipality;
    private int carbonEmissions;
    private int carbonAbsorption;
    private int total;
    private int electricity;
    private int greenhouseGas;
	
	public CbiVO(String municipality, int carbonEmissions, int carbonAbsorption, int total, int electricity,
			int greenhouseGas) {
		super();
		this.municipality = municipality;
		this.carbonEmissions = carbonEmissions;
		this.carbonAbsorption = carbonAbsorption;
		this.total = total;
		this.electricity = electricity;
		this.greenhouseGas = greenhouseGas;
	}

	public String getMunicipality() {
		return municipality;
	}

	public void setMunicipality(String municipality) {
		this.municipality = municipality;
	}

	public int getCarbonEmissions() {
		return carbonEmissions;
	}

	public void setCarbonEmissions(int carbonEmissions) {
		this.carbonEmissions = carbonEmissions;
	}

	public int getCarbonAbsorption() {
		return carbonAbsorption;
	}

	public void setCarbonAbsorption(int carbonAbsorption) {
		this.carbonAbsorption = carbonAbsorption;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public int getElectricity() {
		return electricity;
	}

	public void setElectricity(int electricity) {
		this.electricity = electricity;
	}

	public int getGreenhouseGas() {
		return greenhouseGas;
	}

	public void setGreenhouseGas(int greenhouseGas) {
		this.greenhouseGas = greenhouseGas;
	}

	@Override
	public String toString() {
		return "CbiVO [municipality=" + municipality + ", carbonEmissions=" + carbonEmissions + ", carbonAbsorption="
				+ carbonAbsorption + ", total=" + total + ", electricity=" + electricity + ", greenhouseGas="
				+ greenhouseGas + "]";
	}
	

    
}
