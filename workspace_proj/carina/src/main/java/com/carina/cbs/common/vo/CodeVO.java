package com.carina.cbs.common.vo;

public class CodeVO {

	private int code;
	private String category;
	
	public CodeVO(int code, String category) {
		super();
		this.code = code;
		this.category = category;
	}

	public CodeVO() {
		super();
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	@Override
	public String toString() {
		return "CodeVO [code=" + code + ", category=" + category + "]";
	}
	
	
	
}
