package com.carina.cbs.emp.vo;

public class EmpVO {

	    private int year;
	    private String month;
	    private String metro;
	    private String city;
	    private int houseCnt;
	    private double powerUsage;
	    private double bill;
	    private double predictedpowerusage;
		public int getYear() {
			return year;
		}
		public void setYear(int year) {
			this.year = year;
		}
		public String getMonth() {
			return month;
		}
		public void setMonth(String month) {
			this.month = month;
		}
		public String getMetro() {
			return metro;
		}
		public void setMetro(String metro) {
			this.metro = metro;
		}
		public String getCity() {
			return city;
		}
		public void setCity(String city) {
			this.city = city;
		}
		public int getHouseCnt() {
			return houseCnt;
		}
		public void setHouseCnt(int houseCnt) {
			this.houseCnt = houseCnt;
		}
		public double getPowerUsage() {
			return powerUsage;
		}
		public void setPowerUsage(double powerUsage) {
			this.powerUsage = powerUsage;
		}
		public double getBill() {
			return bill;
		}
		public void setBill(double bill) {
			this.bill = bill;
		}
		public double getPredictedpowerusage() {
			return predictedpowerusage;
		}
		public void setPredictedpowerusage(double predictedpowerusage) {
			this.predictedpowerusage = predictedpowerusage;
		}
		@Override
		public String toString() {
			return "EmpVO [year=" + year + ", month=" + month + ", metro=" + metro + ", city=" + city + ", houseCnt="
					+ houseCnt + ", powerUsage=" + powerUsage + ", bill=" + bill + ", predictedpowerusage="
					+ predictedpowerusage + "]";
		}

	    
	    
}
